public class Using_charAt {
    public static void main(String[] args) {
        String User_First_Name = "ADITYA";
        String User_Last_Name = "NIJAVE";
        char User_First_Name_Initial = User_First_Name.charAt(0);
        char User_Last_Name_Initial = User_Last_Name.charAt(0);

        System.out.println(User_First_Name);
        System.out.println(User_Last_Name);
        System.out.println(User_First_Name_Initial);
        System.out.println(User_Last_Name_Initial);
    }
}
